public class Animal {
   private String species;
   private String color;

   public void setSpecies(String inputSpecies) {
      species = inputSpecies;
   }
   
   public void setColor(String inputColor) {
      color = inputColor;
   }
   
   public String getSpecies() {
      return species;
   }
   
   public String getColor() {
      return color;
   }
}